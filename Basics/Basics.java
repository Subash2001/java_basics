public class Basics {
    public static void main(String[] args) {
        // if else
        int n=10;
        if (n==5){ // if this condition is true it will be print or condition is false it should be print else condition
            System.out.println("hello!");
        }
        else {
            System.out.println("World");
        }

        // while loop
        int count = 1;
        while (count !=6){
            System.out.println(count);
            count++;
        }

        // for loop
        for (int i=1; i !=4; i++){
            System.out.println(i);
        }

    }
}
